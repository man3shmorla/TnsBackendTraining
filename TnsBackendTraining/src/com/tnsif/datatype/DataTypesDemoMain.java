package com.tnsif.datatype;

public class DataTypesDemoMain {

	public static void main(String[] args) {
		int value1 = 10 / 2; //integer division
		float value2 = 104 / 61;
		double value3 = 10d / 6d;
		float value4 = 5/2.5f; //float division
		System.out.println("value of 1=" + value1); 
		System.out.println("value of 2=" + value2);
		System.out.println("value of 3=" + value3);
		System.out.println("value of 4=" + value4);
		
		
		int marker = 512;
		//assigning expression to variable 
		double percentage = marker * 0.46f;
		System.out.println("percentage :" + percentage);

	}

}
